<script lang="ts">
  import { timeout } from '../scripts/utils';
  
  export let percentage: number;
  export let text: string = '';

  let percent: number = 0;
  const animatePercent = async () => {
    percent = 0;
    while(percent < Math.floor((percentage * 100))) {
      await timeout(15);
      percent++;
    }
  };

  // Reactive statement to update percentage
  $: if (percentage) {
    animatePercent();
  }
</script>

<div class="chart">
  <svg viewBox="0 0 36 36">
    <path
      d="M18 2.0845
        a 15.9155 15.9155 0 0 1 0 31.831
        a 15.9155 15.9155 0 0 1 0 -31.831"
    />
    <path
      stroke-dasharray={`${percent}, 100`}
      d="M18 2.0845
        a 15.9155 15.9155 0 0 1 0 31.831
        a 15.9155 15.9155 0 0 1 0 -31.831"
    />
  </svg>
  <div class="inset-chart-shadow"></div>
  <div class="chart-info flex align-center justify-center column">
    <h2 class="modal-utilrate">
      {(percentage * 100).toFixed(2)}%
    </h2>
    {#if text}
      <span>
        {text}
      </span>
    {/if}
  </div>
</div>

